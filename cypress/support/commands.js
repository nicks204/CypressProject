Cypress.Commands.add('login', (email, password) => {  

    const uname_elm = '#user-name'
    const pwd_elm = '#password'
    const loginbtn_elm = '#login-button'

    cy.get(uname_elm).type(email);
    cy.get(pwd_elm).type(password);
    cy.get(loginbtn_elm).click();

})